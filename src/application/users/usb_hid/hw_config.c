void USB_SoftConnect(FunctionalState NewState)
{
	if (NewState == ENABLE)
		GPIO_SetBits(USB_SOFT_CONNECT_GPIO, USB_SOFT_CONNECT_GPIO_PIN);
	else
		GPIO_ResetBits(USB_SOFT_CONNECT_GPIO, USB_SOFT_CONNECT_GPIO_PIN);
}

